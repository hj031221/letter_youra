const tree = document.querySelector(".light-tree");

const rows = 6;           // 트리 층 수
const lightsPerRow = 4;    // 최대 전구 개수
const treeWidth = 220;
const treeHeight = 300;

for (let row = 0; row < rows; row++) {
  const y = (row / rows) * treeHeight;

  const rowWidth = (row / rows) * treeWidth;
  const count = Math.ceil((row / rows) * lightsPerRow) + 1;

  for (let i = 0; i < count; i++) {
    const colors = ["red", "yellow", "green"];
    const light = document.createElement("div");
    light.className = "light " + colors[Math.floor(Math.random() * colors.length)];

    const x =
      (treeWidth / 2 - rowWidth / 2) +
      (i / (count - 1 || 1)) * rowWidth;

    light.style.left = `${x}px`;
    light.style.top = `${y}px`;

    /* 반짝임 랜덤 */
    light.style.animationDelay = `${Math.random() * 2}s`;
    light.style.animationDuration = `${2 + Math.random() * 2}s`;

    tree.appendChild(light);
  }
}

